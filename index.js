const questionElement = document.getElementById("question");
const answerButtons = document.getElementById("answer-buttons");
const nextButton = document.getElementById("next-btn");
const playAgainButton = document.getElementById("playAgain-btn");
const app = document.getElementById("app");
const levelTitle = document.querySelector(".container h1");
const level = document.getElementById("level");

let currentLevel;
var x = document.querySelectorAll(".levelPicker");
for (var i = 0; i < x.length; i++) {
  x[i].addEventListener("click", function () {
    currentLevel = this.value;
    console.log(currentLevel);

    if (currentLevel == "" || currentLevel == null) {
      app.style.display = "none";
    } else if (currentLevel == 1) {
      level.style.display = "none";
      levelTitle.style.display = "none";
      app.style.display = "block";
      startQuiz(questionsBeginner);
      handleNextQuestion(questionsBeginner);
    } else if (currentLevel == 2) {
      level.style.display = "none";
      levelTitle.style.display = "none";
      app.style.display = "block";
      startQuiz(questionsImmediate);
      handleNextQuestion(questionsImmediate);
    } else if (currentLevel == 3) {
      level.style.display = "none";
      levelTitle.style.display = "none";
      app.style.display = "block";
      startQuiz(questionsProffesional);
      handleNextQuestion(questionsProffesional);
    } else if (currentLevel == 4) {
      level.style.display = "none";
      levelTitle.style.display = "none";
      app.style.display = "block";
      startQuiz(questionsMaster);
      handleNextQuestion(questionsMaster);
    }
  });
}

let currentQuestionIndex = 0;
let score = 0;

function startQuiz(questionsData) {
  currentQuestionIndex = 0;
  score = 0;
  nextButton.innerHTML = "Next";
  showQuestion(questionsData);
}

function showQuestion(questionsData) {
  resetState();
  console.log(questionsData);
  let currentQuestion = questionsData[currentQuestionIndex];
  let questionNo = currentQuestionIndex + 1;
  questionElement.innerHTML = questionNo + ". " + currentQuestion.question;

  currentQuestion.answer.forEach((answer) => {
    const button = document.createElement("button");
    button.innerHTML = answer.text;
    button.classList.add("btn");
    answerButtons.appendChild(button);
    if (answer.correct) {
      button.dataset.correct = answer.correct;
    }
    button.addEventListener("click", selectAnswer);
  });
}

function resetState() {
  nextButton.style.display = "none";
  while (answerButtons.firstChild) {
    answerButtons.removeChild(answerButtons.firstChild);
  }
}

function selectAnswer(e) {
  const selectedBtn = e.target;
  const isCorrect = selectedBtn.dataset.correct === "true";
  if (isCorrect) {
    selectedBtn.classList.add("correct");
    score++;
  } else {
    selectedBtn.classList.add("incorrect");
  }
  Array.from(answerButtons.children).forEach((button) => {
    if (button.dataset.correct === "true") {
      button.classList.add("correct");
    }
    button.disabled = true;
  });
  nextButton.style.display = "block";
}

function showScore(questionsData) {
  resetState();
  questionElement.innerHTML = `You scored ${score} out of ${questionsData.length}!`;
  nextButton.style.display = "none";
  playAgainButton.style.display = "block";
}

function handleNextButton(questionsData) {
  currentQuestionIndex++;
  if (currentQuestionIndex < questionsData.length) {
    showQuestion(questionsData);
  } else {
    showScore(questionsData);
  }
}

function handleNextQuestion(questionsData) {
  nextButton.addEventListener("click", () => {
    if (currentQuestionIndex < questionsData.length) {
      handleNextButton(questionsData);
    } else {
      startQuiz();
    }
  });
}

playAgainButton.addEventListener("click", () => {
  levelTitle.style.display = "block";
  level.style.display = "flex";
  app.style.display = "none";
});
